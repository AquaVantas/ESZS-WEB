									<h1 style="font-size: 25px;">16. Med tekmo</h1>
									<p>Če katera izmed ekip ni izbrala vseh agentov v postopku izbire agentov in se igra vrne v izhodiščno točko (lobby), morajo biti nastavitve igre nastavljene znova.</p>
									<p>V primeru, da se igra prične preden sta kapetana obeh ekip v pogovorno okno vpisala, da sta ekipi pripravljeni, ima ekipa, ki tega ni storila  možnost, da zahteva ponovno nastavitev igre. Ampak to le v primeru, preden se začne prva runda igre.</p>
									<p>Če kateri izmed igralcev ni povezan v igro preden se odštevanje konča, lahko ekipa zahteva ponovno nastavitev igre.</p>
									<p>Če se je igra pričela na napačni mapi je potrebno začeti znova.</p>
									<p>V primeru, da pride do ponastavljanja igre, morajo vsi tekmovalci zapustiti le to in pričeti na novo kolikor hitro je le mogoče. Oba kapetana, morata ponovno napisati v pogovorno okno igre, da sta ekipi pripravljeni in sporočiti EŠZS adminu. Če ekipa ni pripravljena 5 minut po ponastavitvi je kaznovana ali diskvalificirana. Igra je lahko ponastavljena zgolj enkrat. Po ponastavitvi igra mora biti odigrana, tudi če se težave še vedno pojavljajo. Razmere se še vedno lahko sporočijo na uradni Valorant discord kanal EŠZS.</p>
									<p>V primeru, da ekipa, ki gostuje igro le te ob zahtevi ne poustvari nastavitev oziroma je ne zažene znova, jo lahko nasprotna ekipa prijavi administratorjem na uradnem Valorant EŠZS kanalu. Če administrator ne ugotovi upravičenega razloga, da se igra ni na novo nastavila, lahko kaznuje ali diskvalificira ekipo, ki tega ni storila.</p>
									<p>Če se tekmovalcu med igranjem prekine povezava z internetom, se igra ne ustavlja ali ponovno nastavlja, temveč se nadaljuje.</p>
									<p>Če se pojavijo problemi s strežnikom pri obeh ekipah in je igro zaradi tega ni mogoče končati, je to potrebno prijaviti administratorju.</p>
