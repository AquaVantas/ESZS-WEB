									<p>Organizatorji si pridržujejo pravico, da lahko kot opazovalec spremljajo vsako tekmo. V primeru, da se organizatorji kot opazovalci udeležijo tekme, bosta o tem obveščena oba kapetana ekip. Opazovalci v nobenem drugem primeru niso dovoljeni.</p>
