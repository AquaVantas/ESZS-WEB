					<h1 style="font-size: 30px;">We’ve put together some general tips for feeling better while playing.</h1>
					<br>With the growth of esports in global markets, there is also a growing awareness of the importance of maintaining and maintaining health and fitness. When someone mentions playing video games or esports to you, you probably don’t think about physical activity. However, more and more professional eathletes are taking care of their health and well-being on a daily basis.
					<br><br>
					The ESD is aware of the unpleasant side effects that can be seen in players who play for too long and too often. Well-known esports teams such as Fnatic and Team Vitality have donated large sums of money to the care, fitness, and well-being of their players. At EŠZS, we also want all players to take care of their health and well-being. So we’ve put together some tips on how you can take care of the physical activity.
					<br><br><br>
					<h1 style="font-size: 30px;">General tips</h1>
					<br>Players between the ages of 5 and 18 who are healthy should have at least 60 minutes of physical activity each day. We suggest activities such as e.g. cycling, running, or playing group sports with a ball. Provide physical strength at least three times a week with exercises such as squats, planks, and push-ups.
					<br><br>
					Players between the ages of 19 and 60 who are healthy should have strength exercises at least twice a week and physical activity such as cycling or running for 20 to 30 minutes a day.
					<br><br><br>
					<h1 style="font-size: 30px;">Proper posture</h1>
					<br>Most people sitting behind a computer have the wrong posture. In addition to the incorrect posture, the arms and wrists also suffer from incorrect posture, as they are in the incorrect position. If you play continuously for a long time, it’s a good idea to get up every hour and spread your wrists in a circular motion. We recommend that you place the monitor at eye level and avoid a posture where the spine is curved. Take a break every hour and walk for at least 5 minutes.
					<br><br><br>
					<h1 style="font-size: 30px;">Sight</h1>
					<br>You can quickly forget about time while playing your favorite games. It is important to rest your eyes while playing. If you play for too long, non-stop, you will overload your eyes. This can lead to headaches, dry eyes, and even double vision.
					<br><br>
					The screen you are using should not be too bright. If you play at night, turn on the light near the screen. This will reduce the contrast in the room, which is more pleasing to the eye. Take advantage of moments during the game, such as the end of the round, to direct your gaze as far away from the screen as possible for at least 30 seconds. Take a break every hour by pulling away from the screen.
					<br><br><br>
					<h1 style="font-size: 30px;">Hydration</h1>
					<br>Drink plenty of water while playing. Avoid coffee and sugary drinks as these cause additional dehydration. With about six glasses (2.5 DCL) of water a day, you will ensure adequate hydration. At the same time, you will find that you have more energy and that you are better collected.
					<br><br><br>
					<h1 style="font-size: 30px;">Sleep</h1>
					<br>Quality sleep ensures well-being throughout the day. Do not look at the screen for at least an hour before going to bed so that your body has enough time to calm down before going to bed. If this is not possible, use a program such as f.lux, which makes the light emitted by the screen more pleasant.
					<br><br><br>
					<h1 style="font-size: 30px;">Mental health</h1>
					<br>We know many professional players in esports who have been subjected to great pressure. It can happen to anyone that stress builds up and overloads it. Then it is very important to talk to parents, relatives, and friends or seek professional help if necessary. Remember that this is just a game whose purpose is fun and that your health comes first.
