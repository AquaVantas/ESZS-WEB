					<h1 style="font-size: 30px;">What is Esports?</h1>
					<br>Esports (also electronic sports, competitive gaming, and professional gaming) is an overall term used to describe competition in video games between professional players. Players usually belong to a specific team or sports organization sponsored by different companies. The most common types of video games related to esports include real-time strategies (eg StarCraft II), fighting games (eg Street Fighter IV), sports video games (eg FIFA), first-person shooting games (eg Counter-Strike) and MOBA multiplayer battle arenas (e.g. League of Legends or LoL).
					<br><br>Esports includes individual (StarCraft II) and team (CS: GO) competition with opponents according to a special set of rules. The predominant organizational model of esports focuses on competitions that fans can watch live or broadcast on various online media (e.g. Twitch.tv or Youtube), while professional commentators make it easier to follow the game.
