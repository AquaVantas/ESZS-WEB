									<h1 style="font-size: 25px;">2. Ligaški sistem</h1>
									<p>Prva sezona državnega prvenstva v igri FIFA21 bo razdeljena na ONLINE del in na OFFLINE. Online bodo potekale kvalifikacije za državno prvenstvo in pa redni del tekmovanja, kjer se bo 32 igralcev potegovalo za 8 vstopnic, ki peljejo na veliki finalni turnir na Gospodarskem razstavišču v Ljubljani. Redni del prvenstva bo potekal po sistemu Swiss, kjer bodo tekmovalci razvrščeni v 2 skupini po 16. Po 4 najboljši iz rednega dela iz vsake skupine, gredo na veliki finalni turnir.</p>
									<br>
									<h1 style="font-size: 25px;">3. Prvi del: Kvalifikacije</h1>
									<p>Kvalifikacije bodo potekale v 4 datumih, ki bodo objavljeni naknadno. Prijavite se lahko na vse 4 turnirje, a le, če si predhodno še niste zagotovili mesta na velikem finalu. Kvalifikacije bodo potekale po sistemu skupinskega dela in izločilnih bojev. Razvrščeni boste v skupine s po 4 igralci. Najboljša 2 po skupinskem delu pa napredujeta v izločilne boje, kjer se igrata 2 tekmi HOME-AWAY vse do polfinala, kjer bodo igralci igrali po sistemu BO3 (beast-of-three).</p>
									<br>
									<h1 style="font-size: 25px;">4. Drugi del: Swiss league</h1>
									<p>32 igralcev bo razvrščenih v 2 skupini po 16, kjer dobite nasprotnika, s katerim imate enak WIN SCORE, seveda pa to ne velja za prvo tekmo. Torej v primeru, da prvo tekmo zmagate, dobite nasprotnika, ki je na prvi tekmi prav tako zmagal. V primeru, da prvo tekmo zmagate in drugo izgubite, dobite nasprotnika, ki ima prav tako WIN SCORE 1-1.</p>
									<br>
									<h1 style="font-size: 25px;">5. Tretji del: PlayOff</h1>
									<p>Playoff se igra OFFLINE na Gospodarskem Razstavišču v Ljubljani, kjer se bo 8 igralcev potegovalo za laskavi naslov URADNEGA DRŽAVNEGA PRVAKA V FIFA 21. V PlayOffu bodo igralci razvrščeni v 2 skupini po 4, kjer bodo proti vsakemu nasprotniku iz skupine igrali 2x HOME-AWAY. V izločilnih bojih, kamor se uvrstita prva dva iz vsake skupine, pa bodo igralci igrali po sistemu BO3.</p>
