					<h1>Kaj je e-šport</h1>
<p>E-šport (tudi elektronski šport, tekmovalno igričarstvo in profesionalno igričarstvo) je krovni izraz, ki se uporablja za opis tekmovanja v videoigrah med poklicnimi igralci. Igralci navadno pripadajo določenemu moštvu ali športni organizaciji, ki jih sponzorirajo različna podjetja. Med najpogostejše zvrsti videoiger, povezanih z e-športom, spadajo realno-časovne strategije (npr. StarCraft II), pretepaške igre (npr. Street Fighter IV), športne videoigre (npr. FIFA), prvoosebne strelske igre (npr. Counter-Strike: Global Offensive ali CS:GO) in večigralske bojne arene MOBA (npr. League of Legends ali LoL).</p>

<p>E-šport vključuje individualno (StarCraft II) in ekipno (CS:GO) tekmovanje z nasprotnikom v skladu s posebnim sklopom pravil. Prevladujoči organizacijski model e-športa je osredotočen na tekmovanja, ki si jih lahko navijači ogledajo v živo ali pa preko živega prenosa na različnih spletnih medijih (npr. Twitch.tv ali Youtube), obenem pa profesionalni komentatorji poskrbijo za lažje sledenje igri.</p>

